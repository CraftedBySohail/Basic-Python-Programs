# methods in list

# 1 . append / add element at the end of the list

number1 = [1,2,4,5,5,3,7,6]

number1.append(55)

print(number1)

# 2. sort / sorting the element in ascending order
# it returns none value 
hello = [1,2,4,3,5,42,23,4,4]
hello.sort()
print(hello)

# 3. reverse sorting (decending order)
hello.sort(reverse = True)
print(hello)

# we can also sort the character as well
number1 = ["A","b","C","v"]

number1.sort()
print(number1)