# set is a immutable data type
# we cannot store list and dictionary in set as they are mutable
# it is a collection of unordered item

collection = {1,24,3,2,323,2}
print(collection)
print(type(collection))

# we can store string value in set as well
hello = {"hoe" , 1,23,9.03, '78'}
print(hello)

# printing the length of set
print(len(hello)) # prints the no of items present in set



# empty set
hell1o2 = set()
print(hell1o2)
print(type(hell1o2))